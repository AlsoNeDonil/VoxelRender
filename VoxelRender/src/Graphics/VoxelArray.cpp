	void VoxelArray::SetInstanceData(const float* data, size_t dataSize)
	{
		glBindBuffer(GL_ARRAY_BUFFER, m_DynamicVBO);
		glBufferSubData(GL_ARRAY_BUFFER, 0, dataSize, data);
	}

